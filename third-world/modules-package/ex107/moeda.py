def aumentar(valor, porcentagem):
    resultado =  valor +(valor * (porcentagem/100))
    return resultado


def diminuir(valor, porcentagem):
    resultado =  valor -(valor * (porcentagem/100))
    return resultado


def dobro(valor):
    resultado = valor * 2
    return resultado


def metade(valor):
    resultado = valor/2
    return resultado

